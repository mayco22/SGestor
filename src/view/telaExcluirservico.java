    private void preencherT(){
        DefaultTableModel modelo = (DefaultTableModel) jTable1.getModel();
        
        ServicoDAO c = new ServicoDAO();
        
        for(Servico cli : c.read()) {
            modelo.addRow(new Object[]{
                cli.getNome()
            });
        }
    }
    public void limpaTabela(){
        DefaultTableModel tblRemove = (DefaultTableModel)jTable1.getModel();
        if (tblRemove.getRowCount() > 0){
            for (int i=1;i<=tblRemove.getRowCount();i++){
                tblRemove.removeRow(i);
            }            
        }
    }
