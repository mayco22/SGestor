    private void readTable(){
        DefaultTableModel modelo = (DefaultTableModel) jTablecontatos.getModel();
        ClienteDAO c = new ClienteDAO();
        
        for(Cliente cli : c.read()) {
            modelo.addRow(new Object[]{
                cli.getNome(),
                cli.getTelefone(),
                cli.getCelular(),
                cli.getEmail()
            });
        }
    }

